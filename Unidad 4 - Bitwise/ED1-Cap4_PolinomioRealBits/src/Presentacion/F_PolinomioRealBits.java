    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        monomioingresado();
        int expon, expod;
        MonomioRealBits mon;
        expon = Integer.parseInt(txtExpNumeOM.getText());
        expod = Integer.parseInt(txtExpDenoOM.getText());
        FraccionBits f = new FraccionBits('+', expon, expod);
        if(btnEleccion.getSelectedItem().toString() == "1) P(x)"){
            if(P1.getMonomio(f) != null){
                P1.eliminar(f);
                txtPoli1.setText(P1.toString());
                txtResultado.setText("");
            }else
                JOptionPane.showMessageDialog(null, "No existe monomio con este exponente en este polinomio");
        }else{
            if(P2.getMonomio(f) != null){
                P2.eliminar(f);
                txtPoli1.setText(P2.toString());
                txtResultado.setText("");
            }else
                JOptionPane.showMessageDialog(null, "No existe monomio con este exponente en este polinomio");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

