    private void btnEvaluarXActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEvaluarXActionPerformed
        monomioingresado();
        float valor;
        Integer valorExp = Integer.parseInt(txtValorX.getText());
        if(btnEleccion.getSelectedItem().toString() == "1) P(x)"){
            valor = P1.valorX(valorExp);
            txtResultado.setText(Float.toString(valor));
        }else{
            valor = P2.valorX(valorExp);
            txtResultado.setText(Float.toString(valor));
        }
    }//GEN-LAST:event_btnEvaluarXActionPerformed

    private void btnObtMonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObtMonActionPerformed
        monomioingresado();
        int expon, expod;
        MonomioRealBits mon;
        expon = Integer.parseInt(txtExpNumeOM.getText());
        expod = Integer.parseInt(txtExpDenoOM.getText());
        FraccionBits f = new FraccionBits('+', expon, expod);
        if(btnEleccion.getSelectedItem().toString() == "1) P(x)"){
            if(P1.getMonomio(f) != null){
                mon = P1.getMonomio(f);
                txtResultado.setText(mon.toString());
            }else
                JOptionPane.showMessageDialog(null, "No existe monomio con este exponente en este polinomio");
        }else{
            if(P2.getMonomio(f) != null){
                mon = P2.getMonomio(f);
                txtResultado.setText(mon.toString());
            }else
                JOptionPane.showMessageDialog(null, "No existe monomio con este exponente en este polinomio");
        }
    }//GEN-LAST:event_btnObtMonActionPerformed

