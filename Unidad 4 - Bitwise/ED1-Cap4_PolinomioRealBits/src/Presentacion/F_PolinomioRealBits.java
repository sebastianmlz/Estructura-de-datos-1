    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        monomioingresado();
        MonomioRealBits m;
        if((coefNume != 0)){
            if(btnSigno.getSelectedItem().toString() == "+"){
                m = new MonomioRealBits('+', coefNume, coefDeno, expNume, expDeno);
            }else
                m = new MonomioRealBits('-', coefNume, coefDeno, expNume, expDeno);
            if(btnCrear.getSelectedItem().toString() == "1) P(x)"){
                P1.insertar(m);
                txtPoli1.setText(P1.toString());

            }else{
                P2.insertar(m);
                txtPoli2.setText(P2.toString());
            }
        }else{
            JOptionPane.showMessageDialog(null, "No se puede ingresar un monomio con coeficiente igual a 0");
        }
        
    }//GEN-LAST:event_btnInsertarActionPerformed

